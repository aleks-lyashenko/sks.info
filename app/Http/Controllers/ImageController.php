     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'title' => 'required',
            'path' => 'image',
        ]);

        if($request->hasFile('path')) {
            $folder = date('Y-m-d');
            $path = $request->file('path')->store("images/{$folder}");
        }

        $image = Image::create([
            'title' => $request->title,
            'path' => $path,
        ]);

        $request->session()->flash('success', 'Successful add image');

        return redirect()->route('images.index');
    }

